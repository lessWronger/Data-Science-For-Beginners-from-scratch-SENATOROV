"""Выбор понятных имен переменных."""

# * Понятные и аккуратные имена — один из ключевых факторов читаемости программы. Если вы пишете код не «на один раз», а рассчитываете, что к нему вернётесь вы сами или кто-то другой, выбор имён становится особенно важным. Это зона ответственности программиста: ни Black, ни другие автоформатеры не могут решить, как именно должна называться переменная или функция.
#
# * Способы записи имён
#
# * Существует несколько распространённых стилей:
# 	•	snake_case — слова разделяются подчёркиванием;
# 	•	camelCase — первое слово пишется со строчной буквы, последующие начинаются с заглавной;
# 	•	PascalCase — как camelCase, но с заглавной буквы начинается и первое слово; стиль получил название от языка Pascal.
#
# * Соглашения PEP 8
#
# * В Python приняты следующие договорённости:
# 	•	используйте только ASCII-буквы (латиница без специальных символов);
# 	•	имена модулей должны быть короткими и записываться в нижнем регистре;
# 	•	классы называют в стиле PascalCase;
# 	•	константы пишут заглавными буквами в snake_case;
# 	•	функции, методы и переменные — в нижнем змеином регистре;
# 	•	приватные атрибуты классов начинаются с подчёркивания _.
#
# * О длине имён
#
# * Имя не должно быть ни слишком коротким, ни чрезмерно длинным. Поскольку код чаще читают, чем пишут, лучше слегка «переборщить» с пояснением, чем сделать имя слишком общим. Не стоит сокращать слова и «глотать» буквы.
#
# * Отдельно стоит избегать венгерской нотации — подхода, при котором тип данных зашивается в имя переменной (strName, intCount). В современных языках и IDE эта информация доступна и без подобных префиксов.
#
# * Имена должны хорошо искаться
#
# * Хорошее имя — уникальное и информативное. Чем конкретнее название, тем проще найти нужное место в коде. Оптимально писать код так, чтобы он был понятен людям, для которых английский не родной язык: без шуток, двусмысленностей и излишней креативности.
#
# * Не перекрывайте встроенные имена
#
# * Никогда не используйте встроенные имена Python для собственных переменных или файлов. Часто проблемные примеры: list, str, sum, id, min, max, type, open и другие. Аналогично не стоит называть .py-файлы так же, как популярные сторонние модули.
